
leftOperand = 5;
rightOperand = 6;
operator = '+';

if(operator == '+'){
    console.log(leftOperand + rightOperand)
}
if(operator == '-'){
    console.log(leftOperand - rightOperand)
}
if(operator == '*'){
    console.log(leftOperand * rightOperand)
}
if(operator == '/'){
    console.log(leftOperand / rightOperand)
}
if(operator == '%'){
    console.log(leftOperand % rightOperand)
}
if(operator == '**'){
    console.log(leftOperand ** rightOperand)
}
else{
    console.log("invalid operator !")
}
